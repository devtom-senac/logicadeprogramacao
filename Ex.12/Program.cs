namespace Ex._12
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Ex.12 Imprimir todos numeros impares de 1 a 20 usando laço while;

            int i = 1;

            while (i <= 20)
            {
                Console.WriteLine(i);
                i += 2;
            }
        }
    }
